function romanize(cyrillic) {
   let dict = {
      'а': "a",
      'б': "b",
      'в': "v",
      'г': "g",
      'д': "d",
      'е': "e",
      'ё': "e",
      'ж': "zh",
      'з': "z",
      'и': "i",
      'й': "i",
      'к': "k",
      'л': "l",
      'м': "m",
      'н': "n",
      'о': "o",
      'п': "p",
      'р': "r",
      'с': "s",
      'т': "t",
      'у': "u",
      'ф': "f",
      'х': "kh",
      'ц': "ts",
      'ч': "ch",
      'ш': "sh",
      'щ': "shch",
      'ъ': "ie",
      'ы': "y",
      'ь': "",
      'э': "e",
      'ю': "iu",
      'я': "ia",
   };
   let temp = cyrillic.toLowerCase().replace(/[абвгдеёжзийклмнопрстуфхцчшщъыьэюя]/g, (a) => dict[a]);
   return temp.replace(/(^|\s)[a-z]/g, b => b.toUpperCase());
}